#initializing variables
a = True
b = False

#printing original value of a
print(f"Value of a: {a}")

#printing the value of not of a
print(f"Value of not of a: {not a}")

#printing original value of b
print(f"Value of b: {b}")

#printing the value of not of b
print(f"Value of not of b: {not b}")

#printing value of a OR b
print(f"Value of not of a OR b: {a or b}")

#printing value of a AND b
print(f"Value of not of a and b: {a and b}")